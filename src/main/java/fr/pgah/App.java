package fr.pgah;

public class App {

  public static void main(String[] args) {

    // 1. Avoir le salaire horaire

    int salaire_horaire = 15;

    // 2. Avoir le nombre d'heures travaillées

    int heures_trav = 35;

    // 3. Calcul de (salaire horaire) x (heures travaillées)

    int salaire_brut = salaire_horaire * heures_trav;

    // 4. Affichage du résultat

    System.out.println(salaire_brut);
  }

}
